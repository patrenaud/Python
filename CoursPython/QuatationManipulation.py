# Quote Manipulation
# Demonstrate string methods

quote = "We are going to dream of things"

print("Original quote:")
print(quote)

print("\nUpperCase:")
print(quote.upper())

print("\nLowercase:")
print(quote.lower())

print ("\nTitle:")
print(quote.title())

print("\nWith a minor replacement:")
print(quote.replace("things", "furries"))

print ("\nOriginal quote is still:")
print(quote)

input("\n\nPress enter to exit")
